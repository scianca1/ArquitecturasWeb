package com.example.microviaje.micromonopatines.clases;




public class Ubicacion {


    private Long x;
    private Long y;

    public Ubicacion(Long x, Long y) {
        this.x = x;
        this.y = y;
    }

    public Long getX() {
        return this.x;
    }
    public Long getY() {
        return this.y;
    }
}
